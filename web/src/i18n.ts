const messages = { en, es, de };

export type Locale = keyof typeof messages;

// Locales offered in the selector. Names are endonyms (shown as-is, not translated).
export const availableLocales: { code: Locale; name: string }[] = [
  { code: "en", name: "English" },
  { code: "es", name: "Español" },
  { code: "de", name: "Deutsch" },
];

const STORAGE_KEY = "pulse.locale";
let current: Locale = "en";
const listeners = new Set<() => void>();

function isLocale(v: string | null): v is Locale {
  return v !== null && v in messages;
}

// Pick the initial locale: a stored choice, else the browser language if we have
// it, else English. Call once at boot.
export function initLocale(): void {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (isLocale(stored)) {
      current = stored;
      return;
    }
  } catch {
    // ignore storage errors
  }
  const nav = navigator.language?.slice(0, 2) ?? "";
  current = isLocale(nav) ? nav : "en";
}

export function currentLocale(): Locale {
  return current;
}

export function setLocale(locale: Locale): void {
  if (locale === current) return;
  current = locale;
  try {
    localStorage.setItem(STORAGE_KEY, locale);
  } catch {
    // ignore storage errors
  }
  for (const fn of listeners) fn();
}

// Subscribe to locale changes (AppElement uses this to re-render on switch).
export function onLocaleChange(fn: () => void): () => void {
  listeners.add(fn);
  return () => listeners.delete(fn);
}

// t(key) returns the string for the current locale, falling back to English.
export function t(key: MessageKey): string {
  return messages[current][key] ?? en[key];
}

// Substitute {name} placeholders in a template from a params bag. Values are
// coerced to string; an unknown placeholder is left untouched.
function interpolate(
  template: string,
  params?: Record<string, unknown>,
): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (whole, name: string) =>
    name in params ? String(params[name]) : whole,
  );
}

// Localize a platform LocalizedString (RFC-014): code is looked up in the current
// locale, then English; if neither has the key the API-provided message is the
// fallback. This is the runtime sibling of t() for keys that come from the API
// (the channel-type catalog labels, help text, and unavailable reasons), which
// are not in the statically typed MessageKey set. params interpolate {name}
// placeholders in whichever string we end up using.
export function tDynamic(
  code: string,
  message: string,
  params?: Record<string, unknown>,
): string {
  const table = messages[current] as Record<string, string | undefined>;
  const template =
    table[code] ?? (en as Record<string, string | undefined>)[code] ?? message;
  return interpolate(template, params);
}
